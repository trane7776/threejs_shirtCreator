export const transition = { type: "spring", duration: 0.8 };

export const slideAnimation = (direction) => {
  const transition = { type: "spring", duration: 0.8, delay: 0 };

  const getPosition = (direction) => {
    switch (direction) {
      case "left":
        return { x: -100, y: 0 };
      case "right":
        return { x: 100, y: 0 };
      case "up":
        return { x: 0, y: 100 };
      case "down":
        return { x: 0, y: -100 };
      default:
        return { x: 0, y: 0 };
    }
  };

  return {
    initial: {
      ...getPosition(direction),
      opacity: 0,
      transition: { ...transition, delay: 0.5 },
    },
    animate: {
      x: 0,
      y: 0,
      opacity: 1,
      transition: { ...transition, delay: 0 },
    },
    exit: {
      ...getPosition(direction),
      transition: { ...transition, delay: 0 },
    },
  };
};

